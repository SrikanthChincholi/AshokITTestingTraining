package AshokIT_3.AshokIT_3;

public class SwitchClass {
	public static void main(String[] args) {

		char a = 'e';
		switch (a) {
		case 'a': {
			System.out.println("Value of a is " + a);
			break;
		}
		case 'b': {
			System.out.println("Value of a is " + a);
			break;
		}
		case 'e': {
			System.out.println("Value of a is " + a);
			break;
		}
		default: {
			System.out.println("No value !!");
		}

		}
	}
}
